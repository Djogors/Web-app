// Get references to the dropdowns
const districtSelect = document.getElementById("district");
const municipalitySelect = document.getElementById("municipality");
const parishSelect = document.getElementById("parish");

// Populate the "Distrito" dropdown
function populateDistricts() {
    Object.keys(districtData).forEach(district => {
        const option = document.createElement("option");
        option.value = district;
        option.textContent = district;
        districtSelect.appendChild(option);
    });
}

// Event listener for when the district is changed
districtSelect.addEventListener("change", function () {
    const selectedDistrict = districtSelect.value;

    // Clear the "Conselhos" and "Freguesias" dropdowns
    municipalitySelect.innerHTML = '<option value="" disabled selected>---</option>';
    parishSelect.innerHTML = '<option value="" disabled selected>---</option>';

    // Populate the "Conselhos" dropdown based on the selected district
    if (selectedDistrict && districtData[selectedDistrict]) {
        Object.keys(districtData[selectedDistrict]).forEach(conselho => {
            const option = document.createElement("option");
            option.value = conselho;
            option.textContent = conselho;
            municipalitySelect.appendChild(option);
        });
    }
});

// Event listener for when the municipality is changed
municipalitySelect.addEventListener("change", function () {
    const selectedDistrict = districtSelect.value;
    const selectedMunicipality = municipalitySelect.value;

    // Clear the "Freguesias" dropdown
    parishSelect.innerHTML = '<option value="" disabled selected>---</option>';

    // Populate the "Freguesias" dropdown based on the selected municipality
    if (selectedDistrict && selectedMunicipality && districtData[selectedDistrict][selectedMunicipality]) {
        districtData[selectedDistrict][selectedMunicipality].forEach(freguesia => {
            const option = document.createElement("option");
            option.value = freguesia;
            option.textContent = freguesia;
            parishSelect.appendChild(option);
        });
    }
});

// Initialize the "Distrito" dropdown on page load
populateDistricts();

function prefillLocation(distrito, conselho, freguesia) {
    if (!distrito) return;
    districtSelect.value = distrito;
    districtSelect.dispatchEvent(new Event('change'));
    setTimeout(() => {
        if (conselho) {
            municipalitySelect.value = conselho;
            municipalitySelect.dispatchEvent(new Event('change'));
        }
        setTimeout(() => {
            if (freguesia) {
                parishSelect.value = freguesia;
            }
        }, 50);
    }, 50);
}

document.addEventListener("DOMContentLoaded", function() {
    populateDistricts();
    const pedido = JSON.parse(localStorage.getItem('pedido_para_reportar'));
    if (pedido) {
        prefillLocation(pedido.distrito, pedido.conselho, pedido.freguesia);
    }
});